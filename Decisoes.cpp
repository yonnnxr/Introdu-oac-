#include <string>
#include <iostream>

int main(void) {
    int x = 10;

    if (x > 0) {
        std::cout << "Esse numero e positivo";
    }
    else if (x = 0) {
        std::cout << "Esse numero e nulo";
    }
    else {
        std::cout << "Esse numero e negativo";
    }
    return 0;
}